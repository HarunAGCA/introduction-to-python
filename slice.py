# Start index inclusive and end index exclusive

# Between start and end index
b = "Hello, World!"
print(b[2:5])

# From beginning to end index
b = "Hello, World!"
print(b[:5])

# From start index to end of string
b = "Hello, World!"
print(b[2:])

# Use negative indexes to start the slice from the end of the string
b = "Hello, World!"
print(b[-5:-2])