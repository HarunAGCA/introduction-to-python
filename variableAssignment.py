# Multiple varible assignment with multiple values
x, y, z = "Orange", "Banana", "Cherry"
print(x)
print(y)
print(z)

# Multiple varible assignment with one value
x = y = z = "Orange"
print(x)
print(y)
print(z)

# Unpacking a  list
fruits = ["apple", "banana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)

